"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('André Santos')
print(1,'André "Santos"')

# Aspas duplas
print("André Santos")
print(2, "André 'Santos'")

# Escape
print('André \'Santos\'')

# r
print(r'André \'Santos\'')