# Operador lógico "not"
# Usado para inverter expressões
# not True = False
# not False = True

print(not True) # False
print(not False) # True

# Variável booleana
condicao = True

# Invertendo o valor da condição
if not condicao:
    print("A condição é False")
else:
    print("A condição é True")